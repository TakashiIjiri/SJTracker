// -------------------------------------------------------------
// compute martching cubes on the volume 
// minIdx / maxIdx indicate a bounding box in which iso surface extracted
// -------------------------------------------------------------
template<class T>
void t_MarchingCubes( 
	const int W, 
	const int H, 
	const int D, 
	const T     *vol   ,
	const T      thresh,
	const TVec3 &cubeSize, 
	const int   *minIdx, 
	const int   *maxIdx,
	TMesh &mesh )
{
	int preCount = 0;
	for( int i=0; i < W*H*D; ++i) if( vol[i] > thresh ) preCount++; 
	const int widhei = W* H;
	
	vector< TVec3 > Vs; Vs.reserve( preCount );
	vector< TTriangle> Ps; Ps.reserve( preCount );
	//sampling volume resolution
	const int sv_W = W +1;
	const int sv_H = H +1;
	const int sv_D = D +1;
	//voxelの中心がsampling points
	const double xOff = cubeSize.data[0] / W ;
	const double yOff = cubeSize.data[1] / H;
	const double zOff = cubeSize.data[2] / D ;
	//実際に利用はしないが，pivotが端に来た時に踏みこえがあり得る
	int* svEdgeX = new int[(sv_W+1) * (sv_H+1) * (sv_D+1)];
	int* svEdgeY = new int[(sv_W+1) * (sv_H+1) * (sv_D+1)];
	int* svEdgeZ = new int[(sv_W+1) * (sv_H+1) * (sv_D+1)];
	for( int i=0; i < (sv_W+1) * (sv_H+1) * (sv_D+1); ++i) { svEdgeX[i]= svEdgeY[i]= svEdgeZ[i]= -1;}
	
	int xS, xE, yS, yE, zS, zE;
	if( minIdx != 0 && maxIdx != 0){
		xS = max(0, minIdx[0]);  xE = min( maxIdx[0]+2, sv_W); 
		yS = max(0, minIdx[1]);  yE = min( maxIdx[1]+2, sv_H);
		zS = max(0, minIdx[2]);  zE = min( maxIdx[2]+2, sv_D);
	}else{
		xS = 0; yS = 0;  zS= 0; xE = sv_W;  yE = sv_H; zE = sv_D;
	}

	short caseID = 0;
	double p[  8 ];
	int    v[ 12 ];                            
                                                                              	                                                                                     
	for( int z = zS; z < zE; ++z){
	for( int y = yS; y < yE; ++y)
	for( int x = xS; x < xE; ++x) 
	{
		int svIdx  = x + y*sv_W + z*sv_W*sv_H;

		//8点サンプリング
		int pivVox = (x-1) + (y-1) * W + (z-1) * widhei;
		p[0] = (x==0     ||y==0	    ||z==0     ) ? -1 : vol[pivVox				  ];
		p[1] = (x==sv_W-1||y==0	    ||z==0     ) ? -1 : vol[pivVox + 1		      ];
		p[2] = (x==sv_W-1||y==0	    ||z==sv_D-1) ? -1 : vol[pivVox + 1     + widhei];
		p[3] = (x==0     ||y==0	    ||z==sv_D-1) ? -1 : vol[pivVox         + widhei];
		p[4] = (x==0     ||y==sv_H-1||z==0     ) ? -1 : vol[pivVox     + W         ];
		p[5] = (x==sv_W-1||y==sv_H-1||z==0     ) ? -1 : vol[pivVox + 1 + W         ];
		p[6] = (x==sv_W-1||y==sv_H-1||z==sv_D-1) ? -1 : vol[pivVox + 1 + W + widhei];
		p[7] = (x==0     ||y==sv_H-1||z==sv_D-1) ? -1 : vol[pivVox +   + W + widhei];

		caseID = 0;
		if( p[0] > (double)thresh ) caseID |=   1;
		if( p[1] > (double)thresh ) caseID |=   2;
		if( p[2] > (double)thresh ) caseID |=   4;
		if( p[3] > (double)thresh ) caseID |=   8;
		if( p[4] > (double)thresh ) caseID |=  16;
		if( p[5] > (double)thresh ) caseID |=  32;
		if( p[6] > (double)thresh ) caseID |=  64;
		if( p[7] > (double)thresh ) caseID |= 128;

		if (mcEdgeTable[caseID] == 0) continue;

		//以下詳細はノート参照
		int svNexX = svIdx+1        ;
		int svNexY = svIdx+sv_W     ;
		int svNexZ = svIdx+sv_W*sv_H;
		
		int svNexXY = svIdx+1+sv_W          ;
		int svNexYZ = svIdx  +sv_W+sv_W*sv_H;
		int svNexZX = svIdx+1     +sv_W*sv_H;

		double xPos = x*xOff - xOff*0.5;  double xNext = xPos+xOff;
		double yPos = y*yOff - yOff*0.5;  double yNext = yPos+yOff;
		double zPos = z*zOff - zOff*0.5;  double zNext = zPos+zOff;

		/*1 :0   p0 - p1*/
		/*4 :2   p3 - p2*/
		/*16:4   p4 - p5*/
		/*64:6   p7 - p6*/
		if(mcEdgeTable[caseID]&   1 && svEdgeX[svIdx  ]==-1){svEdgeX[svIdx  ]=(int)Vs.size(); Vs.push_back( TVec3(xPos + xOff*((double)thresh-p[0])/(p[1]-p[0]), yPos , zPos ) );}
		if(mcEdgeTable[caseID]&   4 && svEdgeX[svNexZ ]==-1){svEdgeX[svNexZ ]=(int)Vs.size(); Vs.push_back( TVec3(xPos + xOff*((double)thresh-p[3])/(p[2]-p[3]), yPos , zNext) );}
		if(mcEdgeTable[caseID]&  16 && svEdgeX[svNexY ]==-1){svEdgeX[svNexY ]=(int)Vs.size(); Vs.push_back( TVec3(xPos + xOff*((double)thresh-p[4])/(p[5]-p[4]), yNext, zPos ) );}		
		if(mcEdgeTable[caseID]&  64 && svEdgeX[svNexYZ]==-1){svEdgeX[svNexYZ]=(int)Vs.size(); Vs.push_back( TVec3(xPos + xOff*((double)thresh-p[7])/(p[6]-p[7]), yNext, zNext) );}		
		/*  2:1  p1-p2*/
		/*  8:3  p0-p3*/
		/* 32:5  p5-p6*/
		/*128:7  p4-p7*/
		if(mcEdgeTable[caseID]&   2 && svEdgeZ[svNexX ]==-1){svEdgeZ[svNexX ]=(int)Vs.size(); Vs.push_back( TVec3(xNext, yPos , zPos +zOff*((double)thresh-p[1])/(p[2]-p[1])) );}		
		if(mcEdgeTable[caseID]&   8 && svEdgeZ[svIdx  ]==-1){svEdgeZ[svIdx  ]=(int)Vs.size(); Vs.push_back( TVec3(xPos , yPos , zPos +zOff*((double)thresh-p[0])/(p[3]-p[0])) );}		
		if(mcEdgeTable[caseID]&  32 && svEdgeZ[svNexXY]==-1){svEdgeZ[svNexXY]=(int)Vs.size(); Vs.push_back( TVec3(xNext, yNext, zPos +zOff*((double)thresh-p[5])/(p[6]-p[5])) );}			
		if(mcEdgeTable[caseID]& 128 && svEdgeZ[svNexY ]==-1){svEdgeZ[svNexY ]=(int)Vs.size(); Vs.push_back( TVec3(xPos , yNext, zPos +zOff*((double)thresh-p[4])/(p[7]-p[4])) );}		
		/* 256:8   p0-p4*/
		/* 512:9   p1-p5*/
		/*1024:10  p2-p6*/
		/*2048:11  p3-p7*/
		if(mcEdgeTable[caseID]& 256 && svEdgeY[svIdx  ]==-1){svEdgeY[svIdx  ]=(int)Vs.size(); Vs.push_back( TVec3(xPos , yPos+yOff*((double)thresh-p[0])/(p[4]-p[0]), zPos ) );}		
		if(mcEdgeTable[caseID]& 512 && svEdgeY[svNexX ]==-1){svEdgeY[svNexX ]=(int)Vs.size(); Vs.push_back( TVec3(xNext, yPos+yOff*((double)thresh-p[1])/(p[5]-p[1]), zPos ) );}		
		if(mcEdgeTable[caseID]&1024 && svEdgeY[svNexZX]==-1){svEdgeY[svNexZX]=(int)Vs.size(); Vs.push_back( TVec3(xNext, yPos+yOff*((double)thresh-p[2])/(p[6]-p[2]), zNext) );}		
		if(mcEdgeTable[caseID]&2048 && svEdgeY[svNexZ ]==-1){svEdgeY[svNexZ ]=(int)Vs.size(); Vs.push_back( TVec3(xPos , yPos+yOff*((double)thresh-p[3])/(p[7]-p[3]), zNext) );}		
	
		v[0] = svEdgeX[svIdx  ];
		v[2] = svEdgeX[svNexZ ];
		v[4] = svEdgeX[svNexY ];
		v[6] = svEdgeX[svNexYZ];

		v[1] = svEdgeZ[svNexX ];
		v[3] = svEdgeZ[svIdx  ];
		v[5] = svEdgeZ[svNexXY];
		v[7] = svEdgeZ[svNexY ];

		v[8] = svEdgeY[svIdx  ];
		v[9] = svEdgeY[svNexX ];	
		v[10]= svEdgeY[svNexZX];
		v[11]= svEdgeY[svNexZ ];

		//polygon生成
		for (int i=0; mcTriTable[caseID][i]!=-1;i+=3) 
		{
			Ps.push_back( TTriangle( v[mcTriTable[caseID][ i ]], 
				                     v[mcTriTable[caseID][i+1]], 
									 v[mcTriTable[caseID][i+2]] ) );
		}
	}
		if( z % 50 == 0) t_info( "%d/%d done...\n",z , zE-zS );
	}


	t_info( "Mesh size vtx: %d  polys : %d\n", Vs.size(), Ps.size() );
	t_info( "Initialize surface data...");
    mesh.init( Vs, Ps );
    t_info( "done...\n");
	delete[] svEdgeX; 
	delete[] svEdgeY; 
	delete[] svEdgeZ;
}



